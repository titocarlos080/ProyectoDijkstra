    private void mostraMatriz() throws ExceptionAristaNoExiste {
        int n = AdyacenciaConPeso.getColumnCount();
        Double m[][] = new Double[n][n];
        for (int i = 0; i < migrafo.cantidadDeVertices(); i++) {
            for (int j = 0; j < migrafo.cantidadDeVertices(); j++) {
                m[i][j] = 0.0;
            }
        }
        for (int i = 0; i < n; i++) {
            Iterable<Integer> lista = migrafo.adyacentesDelVertice(i);
            for (Integer j : lista) {
                m[i][j] = migrafo.peso(i, j);
            }

        }
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                if (i == j) {
                    AdyacenciaConPeso.setValueAt(0 + "Bs", i, j);
                } else {
                    if (!migrafo.existeAdyacencia(i, j)) {
                        AdyacenciaConPeso.setValueAt("    °°", i, j);
                    } else {
                        AdyacenciaConPeso.setValueAt(m[i][j] + "Bs", i, j);

                    }
                }

            }

        }
    }

