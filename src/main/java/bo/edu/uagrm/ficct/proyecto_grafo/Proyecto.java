    private String evaluarCamino(int posOrigen, int PosDestino) throws ExceptionAristaNoExiste {
        Dijkstra camino = new Dijkstra(migrafo);
        String inf = "[ ";
        List<Integer> listaDeCaminos = camino.caminosMasCortos(posOrigen, PosDestino);
        for (Integer pos : listaDeCaminos) {
            inf += " -- " + posiciones.get(pos);

        }
        inf += " --] CON UN COSTO DE :" + camino.obtenerCosto();
        return inf;
    }
